public class Task5_UniversAge {
    public static void main(String[] args) {
        long universeAge = 13000000000L;
        long humanityAge = 100000L;
        long ageDifference = universeAge / humanityAge;
        System.out.println("Возраст Вселенной в годах: " + universeAge);
        System.out.println("Возраст человека, как вида: " + humanityAge);
        System.out.println("Во сколько раз Вселенная старше человечества: " + ageDifference);
    }
}
